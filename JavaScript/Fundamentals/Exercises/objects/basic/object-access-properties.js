/*
Create an object car with properties: brand, model, year. Access and print the model using
both dot and bracket notation.
*/

const Car = {
  brand: "BMW",
  model: "BMW Off Road 202",
  year: "2024",
};

// using the dot notation

console.log(Car.brand);
console.log(Car.model);
console.log(Car.year);
console.log("------------------------");

// using the bracket notation

console.log(Car["brand"]);
console.log(Car["year"]);
console.log(Car["model"]);
